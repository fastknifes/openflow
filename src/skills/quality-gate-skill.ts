      '   from the workspace and history. This determines how the gate evaluates readiness.',
      '3. **Reflection Corrective Rule Check** — Reviews relevant corrective rules from',
      '   `docs/current/workflow/ai-reflection/` that may apply to the current task,',
      '   changed files, command flow, or verification behavior. This is an instruction-only',
      '   check: identify applicable rules and factor them into readiness reasoning.',
