      '   — proceed to the next step. Readiness Classification remains the final authority',
      '   on whether implementation is complete.',
      '4. **Harden Decision** — Decides whether adversarial quality hardening is required based on',
      '   change complexity, risk indicators, and cross-cutting concerns.',
      '5. **Evidence Collection** — If harden is needed, the gate runs it and collects results.',
      '6. **Verify Execution** — Runs evidence-aware verification that checks:',
      '   - Drift detection against approved design/requirements',
      '   - Quality checks (typecheck, lint, test, format)',
      '   - Security checks (secrets, vulnerabilities)',
      "   - Consistency with current system facts and decisions",
