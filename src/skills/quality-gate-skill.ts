      '',
      '## When to Invoke',
      '',
      'Invoke this skill AFTER implementation work is complete, including:',
      '- Writing or modifying production/source files for a feature or fix',
      '- Adding or updating tests as part of implementation',
      '- Addressing implementation review feedback',
      '',
      'Do NOT invoke this skill during design-only, planning-only, metadata-only, or feature-renaming work.',
      'Those tasks may need targeted consistency checks, but they are not post-implementation readiness gates.',
      '',
      '## How to Invoke',
      '',
      'Use the internal OpenFlow tool:',
      '',
      '`openflow-quality-gate`',
      '',
      'Pass no arguments. The tool detects context automatically.',
      '',
      '## What the Quality Gate Does',
      '',
      '1. **Applicability Classification** — First decides whether the current work is Applicable, NotApplicable, NeedsWorkflowStage, or LimitedContext.',
